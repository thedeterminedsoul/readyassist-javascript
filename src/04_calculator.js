
function add(n1, n2) {
    let result = n1 + n2
    return result
}

function sub(n1, n2) {
    let result = n1 - n2;
    return result;
}

function mul(n1, n2) {
    let result = n1 * n2;
    return result;
}

function div(n1, n2) {
    let result = n1 / n2;
    return result;
}

let addition_result = add(5, 5)
let subtraction_result = sub(5, 5)
let multiplication_result = mul(5, 5)
let division_result = div(5, 5)

console.log(`Addtion: ${addition_result}`)
console.log(`Subtraction: ${subtraction_result}`)
console.log(`Multiplication: ${multiplication_result}`)
console.log(`Division: ${division_result}`)
